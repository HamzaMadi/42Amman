#include "libft.h"

void	ft_bzero(void *str, size_t n)
{
	unsigned char	*h;
	int				i;

	h = (unsigned char *)str;
	i = 0;
	while (n > 0)
	{
		h[i] = 0;
		i++;
		n--;
	}
}
